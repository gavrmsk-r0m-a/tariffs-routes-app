    new_price_in_provider_currency NUMERIC(18,6) NOT NULL,
    old_conversion_rate_to_eur NUMERIC(20,8),
    new_conversion_rate_to_eur NUMERIC(20,8) NOT NULL,
    old_conversion_rate_date DATE,
    new_conversion_rate_date DATE NOT NULL,
    old_eur_price NUMERIC(18,6),
    new_eur_price NUMERIC(18,6) NOT NULL,
    eur_price_delta NUMERIC(18,6),
    reason TEXT,
    comment TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_tariff_change_history_tariff FOREIGN KEY (tariff_id) REFERENCES tariffs(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariff_change_history_changed_by FOREIGN KEY (changed_by) REFERENCES users(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariff_change_history_country FOREIGN KEY (country_id) REFERENCES countries(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariff_change_history_provider FOREIGN KEY (provider_id) REFERENCES providers(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariff_change_history_prefix FOREIGN KEY (provider_prefix_id) REFERENCES provider_prefixes(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariff_change_history_old_currency FOREIGN KEY (old_provider_currency_id) REFERENCES currencies(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariff_change_history_new_currency FOREIGN KEY (new_provider_currency_id) REFERENCES currencies(id) ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS phone_numbers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    country_id BIGINT NOT NULL,
    provider_id BIGINT,
    country_label TEXT,
    provider_label TEXT,
    number TEXT NOT NULL,
    normalized_number TEXT NOT NULL,
    project_label TEXT,
    assignment_type TEXT,
    assignment_label TEXT,
    phone_type TEXT,
    tariff_label TEXT,
    status TEXT NOT NULL DEFAULT 'unknown',
    connection_cost NUMERIC(18,6),
    monthly_fee NUMERIC(18,6),
    outgoing_rate NUMERIC(18,6),
    incoming_rate NUMERIC(18,6),
    currency_id BIGINT,
    currency_label TEXT,
    comment TEXT,
    is_active BOOLEAN NOT NULL DEFAULT true,
    review_required BOOLEAN NOT NULL DEFAULT false,
    imported_created_by TEXT,
    created_by BIGINT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_by BIGINT,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    deactivated_at TIMESTAMPTZ,
    CONSTRAINT uq_phone_numbers_normalized_number UNIQUE (normalized_number),
    CONSTRAINT fk_phone_numbers_country FOREIGN KEY (country_id) REFERENCES countries(id) ON DELETE RESTRICT,
    CONSTRAINT fk_phone_numbers_provider FOREIGN KEY (provider_id) REFERENCES providers(id) ON DELETE RESTRICT,
    CONSTRAINT fk_phone_numbers_currency FOREIGN KEY (currency_id) REFERENCES currencies(id) ON DELETE RESTRICT,
    CONSTRAINT fk_phone_numbers_created_by FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE RESTRICT,
    CONSTRAINT fk_phone_numbers_updated_by FOREIGN KEY (updated_by) REFERENCES users(id) ON DELETE RESTRICT,
    CONSTRAINT ck_phone_numbers_status CHECK (status IN ('used', 'unused', 'free', 'problem', 'unknown')),
    CONSTRAINT ck_phone_numbers_costs_nonnegative CHECK ((connection_cost IS NULL OR connection_cost >= 0) AND (monthly_fee IS NULL OR monthly_fee >= 0) AND (outgoing_rate IS NULL OR outgoing_rate >= 0) AND (incoming_rate IS NULL OR incoming_rate >= 0)),
    CONSTRAINT ck_phone_numbers_number_format CHECK (number ~ '^[1-9][0-9]{6,20}$'),
    CONSTRAINT ck_phone_numbers_normalized_matches CHECK (normalized_number = number)
);

