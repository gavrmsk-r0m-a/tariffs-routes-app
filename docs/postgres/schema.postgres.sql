-- PostgreSQL schema draft for TeleRoute.
-- Stage 9 artifact only: DDL draft, no seed data, no runtime switch.
-- Source: app/schema.sql as of this branch.

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username TEXT NOT NULL,
    email TEXT,
    display_name TEXT NOT NULL,
    role_key TEXT NOT NULL DEFAULT 'operator',
    role TEXT,
    must_change_password BOOLEAN NOT NULL DEFAULT false,
    is_active BOOLEAN NOT NULL DEFAULT true,
    password_hash TEXT,
    password_salt TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT uq_users_username UNIQUE (username)
);


CREATE TABLE IF NOT EXISTS demo_data_state (
    key TEXT PRIMARY KEY,
    value TEXT NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS user_permissions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    section_key TEXT NOT NULL,
    can_read BOOLEAN NOT NULL DEFAULT false,
    can_write BOOLEAN NOT NULL DEFAULT false,
    can_export BOOLEAN NOT NULL DEFAULT false,
    CONSTRAINT fk_user_permissions_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT uq_user_permissions_user_section UNIQUE (user_id, section_key)
);

CREATE TABLE IF NOT EXISTS countries (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    code TEXT,
    is_active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT uq_countries_name UNIQUE (name)
);

CREATE TABLE IF NOT EXISTS currencies (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT NOT NULL,
    name TEXT NOT NULL,
    symbol TEXT,
    is_active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT uq_currencies_code UNIQUE (code)
);

CREATE TABLE IF NOT EXISTS currency_rates (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    currency_id BIGINT NOT NULL,
    rate_to_eur NUMERIC(20,8) NOT NULL,
    rate_date DATE NOT NULL,
    updated_by BIGINT NOT NULL,
    comment TEXT,
    source TEXT NOT NULL DEFAULT 'manual',
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_currency_rates_currency FOREIGN KEY (currency_id) REFERENCES currencies(id) ON DELETE RESTRICT,
    CONSTRAINT fk_currency_rates_updated_by FOREIGN KEY (updated_by) REFERENCES users(id) ON DELETE RESTRICT,
    CONSTRAINT ck_currency_rates_rate_positive CHECK (rate_to_eur > 0),
    CONSTRAINT ck_currency_rates_source CHECK (source IN ('manual', 'api', 'import'))
);

CREATE TABLE IF NOT EXISTS providers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    normalized_name TEXT NOT NULL,
    provider_type TEXT NOT NULL DEFAULT 'unknown',
    default_currency_id BIGINT,
    is_active BOOLEAN NOT NULL DEFAULT true,
    comment TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT uq_providers_normalized_name UNIQUE (normalized_name),
    CONSTRAINT fk_providers_default_currency FOREIGN KEY (default_currency_id) REFERENCES currencies(id) ON DELETE RESTRICT,
    CONSTRAINT ck_providers_provider_type CHECK (provider_type IN ('voip', 'sms', 'gateway', 'other', 'unknown'))
);

CREATE TABLE IF NOT EXISTS provider_prefixes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_id BIGINT NOT NULL,
    prefix TEXT,
    name TEXT,
    comment TEXT,
    is_active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_provider_prefixes_provider FOREIGN KEY (provider_id) REFERENCES providers(id) ON DELETE RESTRICT
);
CREATE UNIQUE INDEX IF NOT EXISTS ux_provider_prefixes_provider_prefix
    ON provider_prefixes(provider_id, COALESCE(prefix, ''));

CREATE TABLE IF NOT EXISTS routes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    country_id BIGINT NOT NULL,
    provider_id BIGINT NOT NULL,
    provider_prefix_id BIGINT,
    name TEXT NOT NULL,
    project_label TEXT,
    cli_source_type TEXT NOT NULL,
    cli_source_label TEXT NOT NULL,
    aon_pool TEXT,
    rnd_type TEXT,
    rnd_pool_owner TEXT,
    comment TEXT,
    is_actual BOOLEAN NOT NULL DEFAULT true,
    priority_status TEXT NOT NULL DEFAULT 'unknown',
    inbound_line_available BOOLEAN NOT NULL DEFAULT false,
    created_by BIGINT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_by BIGINT,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_routes_country FOREIGN KEY (country_id) REFERENCES countries(id) ON DELETE RESTRICT,
    CONSTRAINT fk_routes_provider FOREIGN KEY (provider_id) REFERENCES providers(id) ON DELETE RESTRICT,
    CONSTRAINT fk_routes_provider_prefix FOREIGN KEY (provider_prefix_id) REFERENCES provider_prefixes(id) ON DELETE RESTRICT,
    CONSTRAINT fk_routes_created_by FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE RESTRICT,
    CONSTRAINT fk_routes_updated_by FOREIGN KEY (updated_by) REFERENCES users(id) ON DELETE RESTRICT,
    CONSTRAINT uq_routes_country_name UNIQUE (country_id, name),
    CONSTRAINT ck_routes_cli_source_type CHECK (cli_source_type IN ('rnd', 'pool', 'sim', 'single_number', 'other')),
    CONSTRAINT ck_routes_rnd_type CHECK (rnd_type IN ('local', 'nonlocal') OR rnd_type IS NULL),
    CONSTRAINT ck_routes_priority_status CHECK (priority_status IN ('priority', 'alternative', 'normal', 'unknown'))
);

CREATE TABLE IF NOT EXISTS route_naming_rules (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    template TEXT NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT false,
    comment TEXT,
    created_by BIGINT NOT NULL,
