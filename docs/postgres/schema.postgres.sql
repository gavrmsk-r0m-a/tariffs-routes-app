    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT uq_route_naming_rules_name UNIQUE (name),
    CONSTRAINT fk_route_naming_rules_created_by FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE RESTRICT,
    CONSTRAINT fk_route_naming_rules_updated_by FOREIGN KEY (updated_by) REFERENCES users(id) ON DELETE RESTRICT
);
CREATE UNIQUE INDEX IF NOT EXISTS ux_route_naming_rules_single_active
    ON route_naming_rules(is_active) WHERE is_active IS TRUE;

CREATE TABLE IF NOT EXISTS tariffs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    country_id BIGINT NOT NULL,
    provider_id BIGINT NOT NULL,
    provider_prefix_id BIGINT,
    provider_currency_id BIGINT NOT NULL,
    price_in_provider_currency NUMERIC(18,6) NOT NULL,
    conversion_rate_to_eur NUMERIC(20,8) NOT NULL,
    conversion_rate_date DATE NOT NULL,
    currency_rate_id BIGINT,
    eur_price NUMERIC(18,6) NOT NULL,
    priority_status TEXT NOT NULL DEFAULT 'unknown',
    is_estimated BOOLEAN NOT NULL DEFAULT false,
    comment TEXT,
    valid_from TIMESTAMPTZ,
    valid_to TIMESTAMPTZ,
    is_current BOOLEAN NOT NULL DEFAULT true,
    created_by BIGINT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_by BIGINT,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_tariffs_country FOREIGN KEY (country_id) REFERENCES countries(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariffs_provider FOREIGN KEY (provider_id) REFERENCES providers(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariffs_provider_prefix FOREIGN KEY (provider_prefix_id) REFERENCES provider_prefixes(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariffs_provider_currency FOREIGN KEY (provider_currency_id) REFERENCES currencies(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariffs_currency_rate FOREIGN KEY (currency_rate_id) REFERENCES currency_rates(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariffs_created_by FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE RESTRICT,
    CONSTRAINT fk_tariffs_updated_by FOREIGN KEY (updated_by) REFERENCES users(id) ON DELETE RESTRICT,
    CONSTRAINT ck_tariffs_price_nonnegative CHECK (price_in_provider_currency >= 0),
    CONSTRAINT ck_tariffs_conversion_rate_positive CHECK (conversion_rate_to_eur > 0),
    CONSTRAINT ck_tariffs_eur_price_nonnegative CHECK (eur_price >= 0),
    CONSTRAINT ck_tariffs_priority_status CHECK (priority_status IN ('priority', 'alternative', 'normal', 'unknown'))
);
CREATE UNIQUE INDEX IF NOT EXISTS ux_tariffs_current_business_key
    ON tariffs(country_id, provider_id, COALESCE(provider_prefix_id, 0)) WHERE is_current IS TRUE;

